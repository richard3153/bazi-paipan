_SOLAR_TERM_TABLE = None

def get_solar_term_dates(year: int) -> list:
    """获取某年24节气日期 [(month,day), ...24个]，按小寒起排序"""
    global _SOLAR_TERM_TABLE
    if _SOLAR_TERM_TABLE is None:
        _SOLAR_TERM_TABLE = _build_solar_term_table()
    if year in _SOLAR_TERM_TABLE:
        return _SOLAR_TERM_TABLE[year]
